package Modelo;

/**
 *
 * @author 59397
 */
public class Pedidos {
    private String fechaPedido;
    private int total;

    public Pedidos() {
    }

    public Pedidos(String fechaPedido, int total) {
        this.fechaPedido = fechaPedido;
        this.total = total;
    }

    public String getFechaPedido() {
        return fechaPedido;
    }

    public void setFechaPedido(String fechaPedido) {
        this.fechaPedido = fechaPedido;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    
    
    
}
